# logical operators

# AND operator
a = 3
print(a<5 and a>5)
# true and false

print(a>5 and a<5)
# false and true

print(a>5 and a>6)
# false and false

print(a<4 and a<5)
# true and true

# OR operator

a = 5

print(a<5 or a<4)
# false or false 

print(a<=5 or a<4)
# true or false

print(a<5 or a<=5)
# false or true

print(a<=5 or a<=5)
# true or true

# NOT operator

a = 10

print(not(a>10))




