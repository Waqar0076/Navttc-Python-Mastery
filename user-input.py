# taking input from user 
name = int(input("Enter your Name?")) # input always returns a str

# printing variable through concatenation technique
print("My Name is", name)

# printing variable through f string technique
print(f"My Name is {name}")

# checking type of variable
print(type(name))

# taking user input in int datatype  
age = int(input("Enter your Age?"))
print(f"My Age is {age}")

# checking type of variable
print(type(age))

# taking user input in float datatype  
height = float(input("Enter your height?"))
print(f"My height is {height}")

# checking type of variable
print(type(height))

